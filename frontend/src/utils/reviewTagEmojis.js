export const getReviewTagEmoji = (tag) => {
  if (!tag) return "";
  const key = String(tag).trim();
  return REVIEW_TAG_EMOJI_MAP[key] || "";
};

export const formatReviewTag = (tag) => {
  if (!tag) return "";
  const text = String(tag).trim();
  const emoji = getReviewTagEmoji(text);
  return emoji ? `${emoji} ${text}` : text;
};
